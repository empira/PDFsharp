﻿namespace PdfSharp.Pdf.AcroForms
{
    /// <summary>
    /// Specifies the horizontal Text-Alignment for a <see cref="PdfTextField"/>
    /// </summary>
    public enum PdfAcroFieldTextAlignment
    {
        /// <summary>
        /// Text is left-aligned
        /// </summary>
        Left,
        /// <summary>
        /// Text is centered inside the Field
        /// </summary>
        Center,
        /// <summary>
        /// Text is right-aligned
        /// </summary>
        Right
    }
}
